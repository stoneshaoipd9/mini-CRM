    private void btnDlgSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDlgSaveActionPerformed
        // TODO add your handling code here:
        String firstName = tfDlgFName.getText();
        String lastName = tfDlgLName.getText();
        String address = tfDlgAddress.getText();
        String postalCode = tfDlgPostalCode.getText();
        String phoneNumber = tfDlgPhoneNo.getText();
        String SIN = tfDlgSIN.getText();

        try {
            db.addCustomer(firstName, lastName, address, postalCode, phoneNumber, SIN);
            dlgAddCustomer.setVisible(false);
            refreshCustomerList();
            tfDlgFName.setText("");
            tfDlgLName.setText("");
            tfDlgAddress.setText("");
            tfDlgPostalCode.setText("");
            tfDlgPhoneNo.setText("");
            tfDlgSIN.setText("");
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this,
                    "Failed execute database query",
                    "Database error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnDlgSaveActionPerformed

