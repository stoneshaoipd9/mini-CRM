    private void refreshRepList() {
        try {
            ArrayList<Representative> repList = db.getAllReps();
            modelRepList.clear();
            for (Representative r : repList) {
                modelRepList.addElement(r);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this,
                    "Failed execute database query",
                    "Database error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

    private void refreshCustomerList() {
        try {
            ArrayList<Customer> customerList = db.getAllCustomers();
            modelCustomerList.clear();
            for (Customer c : customerList) {
                modelCustomerList.addElement(c);
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this,
                    "Failed execute database query",
                    "Database error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

    private void enableButtonWhenBothListsSelected() {
        int idx1 = lstCustomerRep.getSelectedIndex();
        int idx2 = lstFindCustomer.getSelectedIndex();
        boolean enabled = idx1 >= 0 && idx2 >= 0;
        btnInteraction.setEnabled(enabled);
    }
