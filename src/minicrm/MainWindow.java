    private void lstInteractionValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstInteractionValueChanged
        // TODO add your handling code here:
        int idx = lstInteraction.getSelectedIndex();
        if (idx >= 0){
            lblForRepName.setText(lblForRepresentative.getText());
            Interaction I = modelInteractionList.get(idx);
            lblForInteraction.setText(I.date);
            TANotes.setText(I.notes);
            TANotes.setEditable(false);
            TAAddNotes.setText(I.notes);
            TAAddNotes.setEditable(true);
        }
    }//GEN-LAST:event_lstInteractionValueChanged

